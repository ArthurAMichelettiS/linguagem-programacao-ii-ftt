public class AutoboxingUnboxing {

    
    public static void main(String[] args) {
        Integer istInt = new Integer(1); 
        Integer secondInt = new Integer(1); 
   
        if (istInt == secondInt) { 
            System.out.println("são iguais"); 
   
        } else { 
            System.out.println("não são iguais"); 
        } 
    }
}
